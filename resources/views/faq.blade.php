                        enkripsi. Pelaku penyebar ransomware lalu akan memeras korban dengan meminta tebusan dalam
                        jumlah tertentu untuk membuka kunci tersebut. <br><br>

                        Menurut Direktur Network dan IT Solutions Telkom Herlan Wirjanako, ransomware Brain Cipher ini
                        mengunci data PDN dan meminta tebusan sebesar 8 juta dollar AS (sekitar Rp 131,2 miliar).<br><br>

                        "Jadi memang di dark web (situs gelap) itu ada jalan dan kita ikuti, mereka (pihak yang menyebar
                        ransomware) minta tebusan ada 8 juta dollar AS. Demikian," kata Herlan. <br><br>

                        Investigasi menyeluruh<br><br>

                        Kini, BSSN bersama Kemkominfo, Cyber Crime Kepolisian RI (Polri), dan KSO Telkomsigma masih
                        terus berproses mengupayakan investigasi secara menyeluruh.<br><br>

                        "Masih mengupayakan investigasi secara menyeluruh pada bukti-bukti forensik yang didapat, dengan
                        segala keterbatasan barang bukti. Karena kondisinya barang bukti terenkripsi. Ini juga harus
                        kami pecahkan," lanjut Hinsa.<br><br>

                        Selain investigasi, tim gabungan BSSN masih berupaya mengatasi dampak dari serangan tersebut,
                        termasuk memulihkan data yang terkunci serta layanan publik terdampak.<br><br>

                        “Upaya-upaya ke sana sudah kami rumuskan dan kami diskusikan tadi, sehingga diharapkan bisa
                        dengan cepat masalah ini, kejadian ini bisa diatasi dengan baik,” kata Hinsa.<br><br>

                        Hinsa juga melaporkan kemajuan upaya yang dilakukan pemerintah per Senin (24/6/2024) pagi
                        ini.<br><br>

